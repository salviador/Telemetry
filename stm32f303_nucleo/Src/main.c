int main(void)
{ 
  uint16_t i;
  
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC2_Init();
  MX_SPI1_Init();

  /* USER CODE BEGIN 2 */
  init_gen_sinewave();
  init_serialChart();
  init_adcConv();

  //HAL_Delay(5000);

  for(i=0;i<6000;i++){
    start_adcConv();    
    WAIT_Conv_IS_DONE();
    hpf_ch1();
    hpf_ch2();
    VRMS = ch1.rms * 0.0008056640625;
  }
  for(i=0;i<6000;i++){
    start_adcConv();    
    WAIT_Conv_IS_DONE();
    hpf_ch1();
    hpf_ch2();
    
    VRMS = ch1.rms * 0.0008056640625000000000000;
   
    VRMSsomma -=  (float)VRMSsomma / (float)100.000000000000;   
    VRMSsomma += (float)VRMS / (float)100.0000000000;
    VRMSm = (float)VRMSsomma;
  }
  
  while (1)
  {
    serialChart_wait_receiveByte(bufferRX);
    
    start_adcConv();    
    WAIT_Conv_IS_DONE();
    hpf_ch1();
    hpf_ch2();
        
    if(bufferRX[0] == 's'){   
      bufferRX[0] = 0x00;
      serialChart_sendJsonTelemetry(ch1, ch2 ,SIZEBUFFERCH);
    }
    
    __NOP();
    __NOP();
    __NOP();
    __NOP();
    
    VRMS = ch1.rms * 0.0008056640625000000000;
    
    VRMSsomma -=  VRMSsomma / 100.00000000;   
    VRMSsomma += VRMS / 100.000000000;
   
    VRMSm = VRMSsomma;
    
    __NOP();
    __NOP();
    __NOP();    
  }

}

/** System Clock Configuration
*/
void SystemClock_Config(void)
{

  RCC_OscInitTypeDef RCC_OscInitStruct;
  RCC_ClkInitTypeDef RCC_ClkInitStruct;
  RCC_PeriphCLKInitTypeDef PeriphClkInit;

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSI;
  RCC_OscInitStruct.HSIState = RCC_HSI_ON;
  RCC_OscInitStruct.HSICalibrationValue = 16;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
  RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSI;
  RCC_OscInitStruct.PLL.PLLMUL = RCC_PLL_MUL16;
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK)
  {
    Error_Handler();
  }

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1|RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV2;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV1;

  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_2) != HAL_OK)
  {
    Error_Handler();
  }

  PeriphClkInit.PeriphClockSelection = RCC_PERIPHCLK_ADC12;
  PeriphClkInit.Adc12ClockSelection = RCC_ADC12PLLCLK_DIV1;
  if (HAL_RCCEx_PeriphCLKConfig(&PeriphClkInit) != HAL_OK)
  {
    Error_Handler();
  }

    /**Configure the Systick interrupt time 
    */
  HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000);

    /**Configure the Systick 
    */
  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

  /* SysTick_IRQn interrupt configuration */
  HAL_NVIC_SetPriority(SysTick_IRQn, 0, 0);
}

